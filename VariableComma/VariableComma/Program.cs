using System;

namespace VariableComma
{
    class Program
    {
        static void Main(string[] args)
        {
            int number1, number2, number3;

            number1 = 1;
            number2 = 2;
            number3 = 3;

            Console.WriteLine("{0}, {1}, {2}", number1, number2, number3);
        }
    }
}
